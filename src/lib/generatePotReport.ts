interface Member {
  user_id: string;
  role: string;
  profiles?: { first_name?: string; avatar_color?: string };
}

interface Transaction {
  id: string;
  user_id: string;
  amount: number;
  created_at: string;
  status: string;
}

interface Withdrawal {
  id: string;
  user_id: string;
  amount: number;
  total_deducted?: number;
  created_at: string;
  status: string;
  note?: string;
}

interface Expense {
  withdrawal_id: string;
  name: string;
  amount: number;
}

interface Pot {
  name: string;
  created_at: string;
  balance: number;
  goal_amount?: number | null;
  currency: string;
}

